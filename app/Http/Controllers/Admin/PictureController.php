    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'file' => 'required|picture|mimes:jpeg,png,jpg,gif,svg|max:2048',
          ]);

          $picture = new Picture();

          if ($request->file('file')) {
            $picturePath = $request->file('file');
            $pictureName = $picturePath->getClientOriginalName();

            $path = $request->file('file')->storeAs('uploads', $pictureName, 'public');
          }

          $picture->name = $pictureName;
          $picture->path = '/storage/'.$path;
          $picture->save();

          return back()->with('success', 'Picture() uploaded successfully');
      }


